	public void accessible(int north, int south, int east, int west, double position)
	{
		String northOption, westOption, southOption, eastOption, up, left, right;

		if (north != 0) 
		{
			northOption = "North: ";
		}
		else 
		{
			northOption = "";
		}

		if (east != 0)
		{
			eastOption = "East: ";
		}
		else 
		{
			eastOption = "";
		}

		if (south != 0)
		{
			southOption = "South: ";
		}
		else 
		{
			southOption = "";
		}

		if (west != 0)
		{
			westOption = "West: ";
		}
		else 
		{
			westOption = "";
		}
	
		if (position == 1 || position == 2 || position == 3)
		{
			up = "Up: ";
		}
		else 
		{
			up = "";
		}
		
		if (position == 4)
		{
			left = "Left: ";
		}
		else
		{
			left = "";
		}
		if (position == 5)
		{
			right ="Right: ";
		}
		else
		{
			right = "";
		}
		view.println("\nExits avalible: " + northOption + westOption + southOption + eastOption + up + left + right);
	}

